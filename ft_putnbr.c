#include "libft.h"

#define ABSOLUTE(X) (X) < 0 ? -(X) : (X)

void		ft_putnbr(int n)
{
	if (n < 0)
		ft_putchar('-');
	if (n / 10 != 0)
		ft_putnbr(ft_absolute(n / 10));
	ft_putchar('0' + ft_absolute(n % 10));
}
