#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	unsigned char		*str;
	size_t				i;

	str = b;
	i = 0;
	while (str[i] != '\0' && len > 0)
	{
		str[i++] = c;
		len--;
	}
	return (b);
}
