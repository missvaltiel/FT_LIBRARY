#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	size_t		dstlen;
	size_t		i;

	dstlen = ft_strlen(dst);
	i = 0;
	while (src[i] != '\0')
	{
		dst[dstlen + i] = src[i];
		i++;
	}
	dst[dstlen + i] = '\0';
	return (dst);
}
